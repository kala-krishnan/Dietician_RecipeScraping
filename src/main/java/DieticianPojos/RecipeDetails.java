package DieticianPojos;

public class RecipeDetails {
	private String recipeId;
	private String recipeName;
	private String recipeTags;
	private String recipePrepTime;
	private String recipeCookTime;
	private String totalTime;
	private String noOfServings;
	private String ingredientDetails;
	private String recipeDescription;
	
	public String getRecipeId() {
		return recipeId;
	}
	public void setRecipeId(String recipeId) {
		this.recipeId = recipeId;
	}
	
	public String getRecipeName() {
		return recipeName;
	}
	public String getRecipeTags() {
		return recipeTags;
	}
	public String getRecipePrepTime() {
		return recipePrepTime;
	}
	public String getRecipeCookTime() {
		return recipeCookTime;
	}
	public String getTotalTime() {
		return totalTime;
	}
	public String getNoOfServings() {
		return noOfServings;
	}
	public String getIngredientDetails() {
		return ingredientDetails;
	}
	public String getRecipeDescription() {
		return recipeDescription;
	}
	public void setRecipeName(String recipeName) {
		this.recipeName = recipeName;
	}
	public void setRecipeTags(String recipeTags) {
		this.recipeTags = recipeTags;
	}
	public void setRecipePrepTime(String recipePrepTime) {
		this.recipePrepTime = recipePrepTime;
	}
	public void setRecipeCookTime(String recipeCookTime) {
		this.recipeCookTime = recipeCookTime;
	}
	public void setTotalTime(String totalTime) {
		this.totalTime = totalTime;
	}
	public void setNoOfServings(String noOfServings) {
		this.noOfServings = noOfServings;
	}
	public void setIngredientDetails(String ingredientDetails) {
		this.ingredientDetails = ingredientDetails;
	}
	public void setRecipeDescription(String recipeDescription) {
		this.recipeDescription = recipeDescription;
	}
	

}
